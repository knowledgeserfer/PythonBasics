#The intersection() method will return a new set, that only 
#contains the items that are present in both sets.
varCarSet1 = {"Dodge", "Ford", "Toyota"}
varCarSet2 = {"Honda", "Dodge", "Ford", "Lincoln"}
varCarSet3 = varCarSet1.intersection(varCarSet2)
print(varCarSet1)
print(varCarSet2)
print(varCarSet3)

