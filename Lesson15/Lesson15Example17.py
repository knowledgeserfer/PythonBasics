#The symmetric_difference() method will return a new set, that 
#contains only the elements that are NOT present in both sets.
varCarSet1 = {"Dodge", "Ford", "Toyota"}
varCarSet2 = {"Honda", "Dodge", "Lincoln"}
varCarSet3 = varCarSet1.symmetric_difference(varCarSet2)
print(varCarSet1)
print(varCarSet2)
print(varCarSet3)


