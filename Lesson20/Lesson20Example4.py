#Python has a set of built-in math functions, including an extensive 
#math module, that allows you to perform mathematical tasks on numbers.
#The min() and max() functions can be used to find the lowest or 
#highest value in an iterable
numList1 = [10,5,25,15,35]
minValue = min(numList1)
maxValue = max(numList1)
maxValue2 = max(5,25,10)
print(minValue)
print(maxValue)
print(maxValue2)


