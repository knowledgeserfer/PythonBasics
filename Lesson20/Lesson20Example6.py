#Python has also a built-in module called math, which extends 
#the list of mathematical functions.
#When you have imported the math module, you can start using methods 
#and constants of the module.
#The math.sqrt() method returns the square root of a number
#The math.ceil() method rounds a number upwards to its nearest integer, 
#and the math.floor() method rounds a number downwards to its 
#nearest integer, and returns the result
import math

num1 = math.sqrt(225)
num2 = math.ceil(3.3)
num3 = math.floor(3.3)
print(num1)
print(num2)
print(num3)