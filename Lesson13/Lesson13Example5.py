#you can access them by referring to the index number
varCarList1 = ["Dodge", "Ford", "Toyota", "BMW", "Nissan"]
print(varCarList1[0])
print(varCarList1[1])
print(varCarList1[2])
#negative indexing
#-1 refers to the last item, -2 refers to the second last item
print(varCarList1[-1])
print(varCarList1[-2])
print(varCarList1[-3])
#range of Indexes [1:4] [:3] [-3:-1]
print(varCarList1[1:4])
print(varCarList1[:3])
print(varCarList1[-3:-1])
print(varCarList1[2:])


