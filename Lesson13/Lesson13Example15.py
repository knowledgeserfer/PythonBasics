#You can loop through the list items by using a while loop
#Use the len() function to determine the length of the list, 
#then start at 0 and loop your way through the list 
#items by referring to their indexes
varCarList1 = ["Dodge", "Ford", "Toyota"]
counter = 0
while counter<len(varCarList1):
    print(varCarList1[counter])
    counter = counter+1
    


