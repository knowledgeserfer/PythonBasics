#to add tuples use + operator
varCarTuple1 = ("Dodge", "Ford", "Toyota")
varCarTuple2 = ("Alfa Romeo", "BMW")
varCarTuple3 = varCarTuple1+varCarTuple2
print(varCarTuple1)
print(varCarTuple2)
print(varCarTuple3)
#If you want to multiply the content of a tuple a 
#given number of times, you can use the * operator
varCarTuple4 = varCarTuple3*2
print(varCarTuple4)
